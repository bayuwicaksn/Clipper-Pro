logger = logging.getLogger("worker_gpu")

# Supabase initialization removed - using shared SQLModel engine

# Global event untuk Graceful Shutdown
shutdown_event = threading.Event()

# ── Database Update Helper ─────────────────────────────────────────────
def update_job_db(job_id: str, data: dict):
    """Update job status/data using shared CRUD logic."""
    try:
        with Session(engine) as session:
            crud.update_job(session, job_id, data)
            return True
    except Exception as e:
        logger.error(f"[{job_id}] Database update failed: {e}")
        return False


# ── Health Check HTTP Server ──────────────────────────────────────────
class HealthHandler(BaseHTTPRequestHandler):
    def do_GET(self):
        self.send_response(200)
        self.send_header("Content-Type", "application/json")
        self.end_headers()
        status = "ok" if not shutdown_event.is_set() else "shutting_down"
        self.wfile.write(json.dumps({"status": status, "worker": "gpu"}).encode())

    def log_message(self, format, *args):
        pass  # suppress access logs

def start_health_server():
    port = int(os.getenv("PORT", "8080"))
    server = HTTPServer(("0.0.0.0", port), HealthHandler)
    logger.info(f"Health server listening on port {port}")
    
    # Run server until shutdown is triggered
    while not shutdown_event.is_set():
        server.handle_request()


# ── Processing Logic ──────────────────────────────────────────────────
def process_job(job_data: dict) -> None:
    job_id = job_data.get("job_id")
    config = job_data.get("config", {})
    
    if not job_id:
        logger.error("Job data missing job_id")
        return

    logger.info(f"[{job_id}] Received job. Starting processing...")
    workspace_root = os.getenv("CLIPPER_WORKSPACE", "/app/workspace")
    job_dir = os.path.join(workspace_root, job_id)
    os.makedirs(job_dir, exist_ok=True)

    try:
        # 1. Update Status to Processing (Menggunakan Retry)
        update_job_db(job_id, {"status": "processing"})
        
        def progress_callback(step, message, progress):
            logger.info(f"[{job_id}] {step}: {message} ({progress}%)")

        # 3. Run Pipeline Initial Steps
        pipeline = Pipeline(job_dir, config, progress_callback)
        clips_metadata = pipeline.run()

        if not clips_metadata:
            logger.warning(f"[{job_id}] No clips generated by analyzer.")
            update_job_db(job_id, {"status": "completed", "clips": "[]"})
            return

        # 4. Finish with metadata only (Deferred Rendering)
        update_job_db(job_id, {
            "status": "completed",
            "clips": json.dumps(clips_metadata)
        })
        return
        return

    except Exception as e:
        logger.error(f"[{job_id}] Pipeline failed: {e}", exc_info=True)
        try:
            update_job_db(job_id, {"status": "error", "error": str(e)})
        except Exception as db_err:
            logger.error(f"[{job_id}] Gagal menyimpan status error ke DB: {db_err}")


def push_to_caption_queue(job_id, job_dir, clips, config):
    project_id = os.getenv("GCP_PROJECT_ID")
    topic_id = os.getenv("PUBSUB_TOPIC_CAPTION") or "clipper-caption-jobs"

    if not project_id:
        logger.warning(f"[{job_id}] GCP_PROJECT_ID not set, skipping caption queue push")
        return

    try:
        from google.cloud import pubsub_v1
        publisher = pubsub_v1.PublisherClient()
        topic_path = publisher.topic_path(project_id, topic_id)
        
        message_data = json.dumps({
            "job_id": job_id, "job_dir": job_dir,
            "clips": clips, "config": config,
            "timestamp": time.time()
        }).encode("utf-8")
        
        publisher.publish(topic_path, message_data).result(timeout=30)
        logger.info(f"[{job_id}] Pushed {len(clips)} clips to captioning topic: {topic_id}")
    except Exception as e:
        logger.error(f"[{job_id}] Failed to push to caption queue: {e}")


# ── Pub/Sub Listener ──────────────────────────────────────────────────
def pull_messages():
    project_id = os.getenv("GCP_PROJECT_ID")
    subscription_id = os.getenv("PUBSUB_SUBSCRIPTION_JOBS") or "clipper-jobs-sub"

    if not project_id:
        logger.warning("GCP_PROJECT_ID not set — running in demo mode")
        while not shutdown_event.is_set():
            time.sleep(10)
        return

    from google.cloud import pubsub_v1
    retry_delay = 10

    while not shutdown_event.is_set():
        try:
            subscriber = pubsub_v1.SubscriberClient()
            subscription_path = subscriber.subscription_path(project_id, subscription_id)
            logger.info(f"Connecting to Pub/Sub: {subscription_path}")

            def callback(message):
                try:
                    data = json.loads(message.data.decode("utf-8"))
                    logger.info(f"Received job: {data.get('job_id')}")
                    process_job(data)
                    message.ack()
                except Exception as e:
                    logger.error(f"Error processing message: {e}", exc_info=True)
                    message.nack()

            streaming_pull_future = subscriber.subscribe(subscription_path, callback=callback)
            logger.info(f"Listening for messages on {subscription_id}...")
            
            with subscriber:
                # Loop until future is done OR shutdown is triggered
                while not shutdown_event.is_set() and not streaming_pull_future.done():
                    time.sleep(1)
                
                # Jika shutdown dipicu, batalkan langganan secara Graceful
                if shutdown_event.is_set():
                    logger.info("Cancelling Pub/Sub subscription due to shutdown...")
                    streaming_pull_future.cancel()
                    streaming_pull_future.result() # Wait to finish canceling without closing RPC channel abruptly

        except Exception as e:
            if not shutdown_event.is_set():
                logger.error(f"Pub/Sub Listener Error: {e}")
                time.sleep(retry_delay)
                retry_delay = min(retry_delay * 2, 300)


def handle_shutdown(signum, frame):
    logger.info("Shutdown signal received! Initiating graceful shutdown...")
    shutdown_event.set()


if __name__ == "__main__":
    # Register Graceful Shutdown signals
    signal.signal(signal.SIGTERM, handle_shutdown)
    signal.signal(signal.SIGINT, handle_shutdown)

    health_thread = threading.Thread(target=start_health_server, daemon=True)
    health_thread.start()

    pull_messages()
    logger.info("Worker process exited cleanly.")